## List
list_arr = [1, 2, 3, 4]

print(type(list_arr), list_arr)

## Dictionary
dict_arr = {
    "doraemon": 129.3,
    "nobita": 130.2,
    "suneo": 128.1,
}

print(type(dict_arr), dict_arr)

## Tuple
tuple_arr = (1, 2, 3, 4, 5)

print(type(tuple_arr), tuple_arr)

